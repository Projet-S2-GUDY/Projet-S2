  <h2>Usage disclaimer and privacy statement<h1>
  <p>the analysis of the resulting data relies on true human guesses. please don't go out of your way to repeatedly and systematically guess the correct correlation (e.g. using bots). it takes away from the enjoyment of other users by spamming the score board. it's also easy to identify users that are doing this from the data and clear their name from the score board. that being said, if you find a bug or something does not work as expected, please do email me so i can sort it out.</p>
