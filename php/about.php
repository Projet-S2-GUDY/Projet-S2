    <br>
  <p>guess the correlation is a game with a purpose. this means, while it aims to be entertaining, data on the guesses is collected and used to analyse how we perceive correlations in scatter plots. so the more people that play, the more data is generated!<p>
    <br>
  <h1>Rules<h1>
    <br>
  <p>guess within 0.05 of the true correlation: +1 life and +5 coins<p>
  <p>guess within 0.10 of the true correlation: +1 coin<p>
  <p>guess within >0.10 of the true correlation: -1 life<p>
    <br>
  <p>you will also receive bonus coins if you make good guesses in a row!<p>
